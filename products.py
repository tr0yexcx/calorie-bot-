def _expand_query(query: str) -> list[str]:
    """Expand query with aliases for better matching."""
    words = query.lower().split()
    expanded = list(words)
    for word in words:
        for stem, aliases in _SEARCH_ALIASES.items():
            if word.startswith(stem):
                expanded.extend(aliases)
    return list(set(expanded))


def find_products(query: str) -> list[tuple[str, tuple[float, float, float, float]]]:
    query = query.lower().strip()
    words = _expand_query(query)

    exact: list[tuple[str, tuple[float, float, float, float]]] = []
    partial: list[tuple[str, tuple[float, float, float, float]]] = []
    seen: set[str] = set()

    for name, kbju in PRODUCTS_DB.items():
        if name in seen:
            continue
        # Full phrase match — top priority
        if query in name:
            exact.insert(0, (name, kbju))
            seen.add(name)
        # All words present
        elif all(w in name for w in query.split() if len(w) >= 3):
            exact.append((name, kbju))
            seen.add(name)
        # Any expanded word matches
        elif any(w in name for w in words if len(w) >= 3):
            partial.append((name, kbju))
            seen.add(name)

    return exact + partial


def get_product(name: str) -> tuple[float, float, float, float] | None:
    return PRODUCTS_DB.get(name.lower().strip())


def calc_kbju(
    kbju_per_100: tuple[float, float, float, float], grams: float
) -> tuple[float, float, float, float]:
    factor = grams / 100
    return tuple(round(v * factor, 1) for v in kbju_per_100)
